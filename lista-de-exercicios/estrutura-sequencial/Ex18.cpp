#include <iostream>
#include <cmath>

using namespace std;

int main() {
    int num;
    cout << "Digite um numero inteiro positivo: ";
    cin >> num;
    cout << "Numero: " << num << endl;
    cout << "Quadrado: " << num * num << endl;
    cout << "Raiz Quadrada: " << sqrt(num) << endl;
    return 0;
}

